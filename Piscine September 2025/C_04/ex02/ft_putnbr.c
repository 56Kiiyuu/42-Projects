#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (nb < 0)
	{
		ft_putchar('-');
		nb = nb * -1;
	}
	if (nb > 9)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	else
	{
		ft_putchar(nb + '0');
	}
}

int	main(void)
{
	ft_putnbr(-2147483649);
	write(1, "\n", 1);
	ft_putnbr(2147483647);
        write(1, "\n", 1);
	ft_putnbr(-56);
        write(1, "\n", 1);
	ft_putnbr(42);
	write(1, "\n", 1);
}

