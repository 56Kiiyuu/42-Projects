#include <unistd.h>

int main(int ac, char **av)
{
	int i;

	i = 0;
	if (ac == 2)
	{
		while (av[1][i])
		{
			if (av[1][i] >= 97 && av[1][i] <= 122)
				write(1, &av[1][i], 1);
			if (av[1][i] >= 65 && av[1][i] <= 90)
			{
				av[1][i] += 32;
				write(1, &av[1][i], 1);
			}
			if (av[1][i + 1] >= 65 && av[1][i + 1] <= 90)
				write(1, "_", 1);
			i++;
		}
	}
	write(1, "\n", 1);
}
