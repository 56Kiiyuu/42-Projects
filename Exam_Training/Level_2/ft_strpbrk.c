char	*ft_strpbrk(const char	*s1, const char	*s2)
{
	int	i = 0;
	int	j;
	char	*re = (char *)s1;

	while (s1[i])
	{
		j = 0;
		while (s2[j])
		{
			if (s2[j] == s1[i])
				return (re);
			j++;
		}
		re++;
		i++;
	}
	return (0);
}
