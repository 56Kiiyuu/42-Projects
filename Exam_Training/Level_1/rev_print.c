#include <unistd.h>

int	main(int ac, char **av)
{
	if (ac == 2)
	{
		int	l;

		l = 0;
		while (av[1][l])
			l++;
		while (l--)
			write(1, &av[1][l], 1);
	}
	write(1, "\n", 1);
}
